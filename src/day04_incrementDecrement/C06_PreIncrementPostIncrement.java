package day04_incrementDecrement;

public class C06_PreIncrementPostIncrement {

	public static void main(String[] args) {
		
		int sayi = 10;
		
		sayi++;
		sayi++;
		sayi++;
		
		System.out.println(sayi);    // 13
		
		System.out.println(sayi++);  // 13
		
		System.out.println(sayi);    // 14
		
		System.out.println(++sayi);  // 15
		
		
		
	}

}
