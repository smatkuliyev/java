package day01_02_variables;

public class C01_Variables {
	
	public static void main(String[] args) {
		
		// istersek tek satır yorum yabalilirim
		/* istersem 
		   çok satırda
		   yazabilirim.
		 */
		
		String studentName = "Sulayman";
		
		System.out.println(studentName);  //Sulayman
		
		int number;
		
		number = 10;
		
		System.out.println(number);         //10
		
		number = 25;
		
		System.out.println(number);        //25
		
		studentName = "Aman";
		
		System.out.println(studentName);   //Aman
		
	}
}
